use telemutter::{FrameWrite, Receiver, Sid, SidMode, parse_frame, write_frame};

#[test]
fn vector_a_parse_sid8_start_minimal() {
    let frame = [0x20, 0x7A, 0x40, 0x01, 0x02, 0x03, 0x04, 0x05];
    let p = parse_frame(&frame, 2, 0).unwrap();
    assert_eq!(p.sid, Some(Sid::Sid8(0x7A)));
    assert_eq!(p.schema_chunk, &[0x40]);
    assert_eq!(p.payload, &[0x01, 0x02, 0x03, 0x04, 0x05]);
}

#[test]
fn vector_b_parse_sid32_start() {
    let frame = [
        0x30, 0x78, 0x56, 0x34, 0x12, 0x40, 0xA0, 0xA1, 0xA2, 0xA3, 0xA4, 0xA5, 0xA6, 0xA7, 0xA8,
        0xA9,
    ];
    let p = parse_frame(&frame, 5, 0).unwrap();
    assert_eq!(p.sid, Some(Sid::Sid32(0x1234_5678)));
    assert_eq!(p.schema_chunk, &[0x40]);
    assert_eq!(
        p.payload,
        &[0xA0, 0xA1, 0xA2, 0xA3, 0xA4, 0xA5, 0xA6, 0xA7, 0xA8, 0xA9]
    );
}

#[test]
fn vector_c_parse_nonstart_padding_alignment() {
    let frame = [
        0x10, 0xC1, 0xC2, 0xA5, 0xA5, 0xA5, 0xB0, 0xB1, 0xB2, 0xB3, 0xB4, 0xB5, 0xB6, 0xB7, 0xB8,
        0xB9,
    ];
    let p = parse_frame(&frame, 5, 0).unwrap();
    assert_eq!(p.sid, None);
    assert_eq!(p.schema_chunk, &[0xC1, 0xC2, 0xA5, 0xA5, 0xA5]);
    assert_eq!(
        p.payload,
        &[0xB0, 0xB1, 0xB2, 0xB3, 0xB4, 0xB5, 0xB6, 0xB7, 0xB8, 0xB9]
    );
}

#[test]
fn vector_d_receiver_valid_sid8_installs_schema() {
    let frame = [0x20, 0x1D, 0x40, 0x01, 0x02, 0x03, 0x04, 0x05];
    let mut r = Receiver::<16>::new(16, 8);
    r.process_frame(&frame, 2).unwrap();
    assert_eq!(r.schema_bytes(), Some(&[0x40][..]));
}

#[test]
fn vector_e_receiver_valid_sid32_two_frame_installs_schema() {
    let f0 = [
        0x30, 0xC0, 0x71, 0x18, 0x02, 0x43, 0x10, 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17, 0x18,
        0x19,
    ];
    let f1 = [
        0x10, 0x01, 0x02, 0x03, 0xA5, 0xA5, 0x20, 0x21, 0x22, 0x23, 0x24, 0x25, 0x26, 0x27, 0x28,
        0x29,
    ];
    let mut r = Receiver::<32>::new(32, 8);
    r.process_frame(&f0, 5).unwrap();
    r.process_frame(&f1, 5).unwrap();
    assert_eq!(r.schema_bytes(), Some(&[0x43, 0x01, 0x02, 0x03][..]));
}

#[test]
fn vector_a_write_matches_exact_hex() {
    let mut out = [0u8; 8];
    write_frame(FrameWrite {
        out_frame: &mut out,
        schema_lane_bytes: 2,
        schema_start: true,
        sid_mode: SidMode::Sid8,
        sid: Some(Sid::Sid8(0x7A)),
        schema_chunk: &[0x40],
        payload: &[0x01, 0x02, 0x03, 0x04, 0x05],
    })
    .unwrap();
    assert_eq!(out, [0x20, 0x7A, 0x40, 0x01, 0x02, 0x03, 0x04, 0x05]);
}

#[test]
fn vector_b_write_matches_exact_hex() {
    let mut out = [0u8; 16];
    write_frame(FrameWrite {
        out_frame: &mut out,
        schema_lane_bytes: 5,
        schema_start: true,
        sid_mode: SidMode::Sid32,
        sid: Some(Sid::Sid32(0x1234_5678)),
        schema_chunk: &[0x40],
        payload: &[0xA0, 0xA1, 0xA2, 0xA3, 0xA4, 0xA5, 0xA6, 0xA7, 0xA8, 0xA9],
    })
    .unwrap();
    assert_eq!(
        out,
        [
            0x30, 0x78, 0x56, 0x34, 0x12, 0x40, 0xA0, 0xA1, 0xA2, 0xA3, 0xA4, 0xA5, 0xA6, 0xA7,
            0xA8, 0xA9
        ]
    );
}
